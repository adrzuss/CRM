	IN `desde` DATE,
	IN `hasta` DATE
)
BEGIN
  SELECT s.nombre, SUM(v.total) as total, COUNT(v.id) AS operaciones, AVG(v.total) op_promedio
  FROM facturav v
  JOIN sucursales s ON v.idsucursal = s.id
  where
    v.fecha BETWEEN desde AND hasta
  GROUP BY s.nombre;  
END//
DELIMITER ;

