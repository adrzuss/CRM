CREATE PROCEDURE `ultimas_10_ventas`(
	IN `sucursal` INT
)
BEGIN

	SELECT f.id, c.nombre, f.total, u.nombre
	FROM facturav f
	JOIN clientes c
		ON f.idcliente = c.id
	JOIN usuarios u
		ON f.idusuario = u.id	
	where
		f.fecha = CURDATE() and
		f.idsucursal = sucursal
	ORDER BY f.id	
	DESC LIMIT 10;
END//
